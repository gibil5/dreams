The nature of my conflict with my mother
The reason of my (apparent) lack of success

My mother is a very conservative person. 
She is used to a way of living that implies a minimum amount of risk taking. 
While I am a liberal person. 
And I am used to a way of living that implies taking risks all the time. 

As a consequence, she understands failure as a defeat. 
While, for me, it is a necessary part of the learning process. 

For years, I have been burdened by her way of thinking. Which was somehow part of myself. 
So, I was half liberal half conservative. 
Analysis, as a process of self-discovery, is teaching me the very important lesson of total 
acceptance of what I really am. Even if it generates incomprehension and doubt. 
I have to understand who I am, and go completely in that direction. 

That is the only way of becoming someone useful to society and, by consequence, 
capable of generating a flow of cash. 

So, do not rely on her approval. And expect lots of doubt and lack of confidence. 
From your mother, your sister. 
Your brother will understand and accept you. But you have to render yourself visible. 

