
Dream
--------
20 junio 2014

Estoy en el lugar de trabajo de Mario Coronado. 
El me ha introducido clandestinamente, para que entre en contacto con el trabajo que hace. 
A primera vista parece la oficina de un abogado, o de un notario. Antes de la invención de
las computadoras. Hay papeles y archivos por todos lados. El desorden es muy grande.
Mario parece inquieto por que me descubran. 

Mario abre un archivo y escoge tres fichas, pero que son como unos paquetes de cartulina. 
Me dice que puedo comenzar a experimentar con eso. Entonces comprendo que las fichas
representan circuitos electrónicos muy valiosos que tenemos que analizar hasta encontrar las fallas. 

Voy al piso superior para intercambiar la ficha por un circuito electrónico. 
Me encuentro con una mujer de piel morena, como de unos cincuenta años. De constitución 
fornida y cabello largo, baja de estatura, con un traje color khaki, con pantaletas cortas. 
Tiene un revolver colgado a la cintura, no muy grande, un poco arcaico. 
Parece una persona bastante agresiva. Comprendo que es la guardiana de los circuitos electrónicos. 

Le entrego las fichas y me da tres paquetes. 

Luego bajo al piso inferior y me encuentro con un muchacho que es el genio de la compañía.
Joven y desgreñado, como de 25 años, delgado. Que me comienza a hablar de ecología. 
Yo lo escucho con paciencia.
De pronto él se da cuenta de que mi interés no es la ecología sino la electrónica 
y de pronto me mira con desconfianza. 

La gente de la compañía circula en ambas direcciones, sin vernos, como si no estuviéramos. 
Están yendo a almorzar al restaurante de la compañía. 




