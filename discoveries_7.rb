

Creo que ya comprendo la naturaleza de lo que me amarra tan fuerte con Alicia. 


Cuando estoy con ella siento que todas mis necesidades son satifechas de manera sorprendentemente eficaz. 
La comida que ella prepara es realmente deliciosa, y tiene una gran capacidad para introducir la limpieza y el orden. 
Sin embargo, a pesar de estos beneficios materiales, yo no siento amor, siento temor. 

Temor porque se que mi salgo algunos centímetros de su visión comenzarán los problemas. 
Los problemas comienzan cuando ella me pregunta mi opinión, y yo le digo lo que pienso. 
Comienza entonces un período de frialdad e indiferencia, que puede durar varias horas, o días, 
con intervalos de inquietante agresividad y violencia. Al final, quien tiene la culpa siempre soy yo. 
Así que tengo que pedir perdón por mis errores imaginarios. 

Sigue un período de gratificación, expresada concretamente en la forma de : comida sabrosa, 
buen sexo, cercanía emocional, una sesión de música juntos, ella cantando y yo tocando la guitarra. 
Pero, efímero y frugal es este paraíso. Porque en cualquier momento se puede romper el equilibrio. 
Comienzan entonces nuevamente los problemas. 










