
# Dream
# --------
# 24 de junio 2014
# Lima


Estoy con Coralie, en París. 
Me dice que se tiene que cambiar de vestido, y que la espere. 
Entonces, sube las gradas de una casa antigua, de cinco pisos, que está al costado de una librería. 
Por causa de la librería, la fachada está cubierta con imágenes en blanco y negro, representando escritores. 
Se ve la imagen de Camus, abrazando a una mujer, como en una película de Holywood. 
Como una carátula de un libro de ediciones Folio. 

Recuerdo que Coralie me ha hablado muchas veces de la fascinación que le genera esa librería. 
Y de como siente que conoce personas cada vez que abre un libro. 

Yo espero y Coralie se demora en bajar. 
Entonces, veo en el segundo piso (1er étage) un hombre que está manipulando a una mujer, 
completamente ebria. La mujer está desmayada, y el hombre no sabe que hacer con ella. 
Están en el balcón, hacia las escaleras, hacia la derecha. Luego, la lleva a su departamento, 
hacia la izquierda, y cierra la puerta. Me pregunto que está sucediendo. 

Bajan y bajan todo tipo de personas. No recuerdo exactamente sus rostros, pero están vestidos como para 
el invierno. 

De pronto, se aparece Coralie, con un vestido de paño marrón, que le queda un poco grande, 
como los que solía usar la tía Carmencita. 
Yo me doy cuenta de que ha rejuvenecido, y parece ahora una niña de 15 años, que me mira con sus ojos 
grandes y abiertos. Siento amor y mucho cariño, y sorpresa. Y la contemplo. Es hermoso. 

(Ingrid Bergman en alguna de sus primeras películas ?)

