
# Dreams 
#--------
#10 de junio 2014

# Escrito apenas despierto, sin permitir que me invadan otros pensamientos

Estoy en la ópera de Paris, que está construída como una espiral
que va subiendo hacia arriba. Poco a poco. Llego tarde y eso hace que los 
guardias me encaminen hacia un asiento que está arriba, pero un poco
alejado del escenario. Me tengo que sentar en un lugar donde hay muchos niños.

Pienso que mis amigos están en algún lugar más abajo, y más cerca del 
escenario, con más espacio. Salgo para buscarlos. Pero luego me olvido. 
Mientras tanto el concierto comienza y termina. Me lo pierdo. 
No se me ocurre llamar por teléfono a mis amigos. Como que esa alternativa no existe. 

Regreso pero el concierto ya terminó. Ocupo el asiento que me habían asignado
y llego a ver a un violoncelista que está ensayando. El ensayo tan solo 
me parece como música de los dioses. Comprendo que me he perdido algo significativo. 

Mientras tanto comienzo a pensar en mi regreso. Hay un conflicto misterioso con 
mi pasaje o con mi visa y tengo que volver a pagar dinero. Me estoy regresando 
al Perú, con todas mis cosas, pero antes tengo que regresar a mi lugar de origen, 
en Francia. Hay un conflicto. 

Se me ocurre hacer algunas compras para mi familia. 
Veo unos pequeños cuadros abstractos muy expresivos, con manchas rojas 
y azules, sobre fondo blanco. Pienso que a Alicia le van a parecer demasiado
simples. Luego encuentro unos objetos pequeños de acero de muy buena calidad. 
El tipo de cosas que no hay forma de encontrar en mi país. 
Son como unos garfios, o como unas mandíbulas, como pequenhas palas mecánicas, 
con dientes y bordes afilados. 
Tengo ganas de llevarme varios. Pero al final no compro nada. Me quedo con una sensación
de desasosiego. 

En algún momento, me encuentro sobre una colina, pero no se ve la ciudad. 
Me cruzo con personas que están caminando hacia las montañas. Recuerdo a un 
hombre alto y fornido, de cabello negro. Se parece a un Tarzán de los años cincuenta. 
Pero con ropa alpina. Recuerdo su rostro con claridad. Está sonriendo benévolamente. 
Se parece a una versión joven del actor alemán que hizo "el gato" y "los últimos días de hitler"

En ese momento regreso a la ópera y cuando quiero salir la gente ha comenzado ya a salir también. 
Comienzan a amontonarse y yo bajo rápido, porque sino voy a perder mi tren. 

Cuando llego a la plataforma ya es demasiado tarde. Pero al mismo tiempo todavía no ha pasado. 
Me doy cuenta que la policía ha puesto un comentario en la extensión de mi visa. 
Algo como podría aprender mejor el francés. Me pongo furioso, porque en realidad 
hablo muy bien el francés. 

En ese momento veo unas ruinas muy hermosas y muy antiguas. De la época romana. 
Han sido reconstruídas y se han caído varias veces. Veo otros monumentos hermosos
en París. El sueño se termina. Es un atardecer con luces laterales y hermosas, que 
afilan los contrastes de los muros y los edificios. 

