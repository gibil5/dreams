
# Sueño: 18 junio 2014
# ----------------------


As long as you have a relationship you are going to be vulnerable. 
(hidden voice saying something clearly)

First scene is a pinup drawing a man. While another man is observing. 
I think that the world is head over heels. 


La primera escena es estática : una pinup dibujando a un hombre. Ella está sentada sobre un banco alto. 
El está echado sobre un sofá. Observa un segundo hombre con una pipa. 
La estética es de los años sesenta, a colores, pero recién comenzando. 

La segunda escena es una casa de un piso en los años cincuenta. Como una película en blanco y negro.
Estoy conversando con un amigo. Ambos tenemos pantalones grises y camisas claras. Como treinta y veinticinco años. 
El cabello negro y engominado. Estamos solos. Pero ha habido otras personas. 
Como restos de una relación flotan en el ambiente. 

Mi amigo me dice en inglés :
"I want to re-write the past"
"Quiero reescribir el pasado"

Entonces, cogemos un libro muy grande de tapas blancas y comenzamos a buscar hacia atrás. 
Me doy cuenta que los dibujos son de tipo comic, esbozos sin color y sin forma. 
Tan solo trazos de carboncillo sobre papel blanco. 
Veo muchas escenas de violencia. Siento temor.  
Veo a un hombre con poderes (como superman o batman) torturado, suspendido del techo por una cuerda, 
con las manos atadas por detrás. 
Luego lo veo sobre un pila de cadáveres. Todos sin ojos. En el lugar de los ojos hay cruces.
Para significar la ausencia.  
Entonces comprendo que son los cadáveres de todas las personas que ha amado, su familia, que han 
sido torturados, cegados y muertos, por algun hombre sin rostro. 

En ese momento comprendo que ese hombre con super-poderes, pero atado de manos 
y muerto y sin ojos, soy yo. Y que los otros son lo queda de mi familia : 
super-mujer, super-niña, super-niño, super-gato y super-perro. 


Escucho de manera insistente uno de los kirtans que nos hizo cantar Gopal. 


