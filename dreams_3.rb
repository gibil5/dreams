
# Dreams
# Trying to get clean in a dirty place

12 de junio 2014


Estoy en un gimnasio, oscuro, con paredes frías y casilleros de metal. 
Estoy haciendo pesas. Mancuernas exactamente. Primero un brazo, después el otro. 
Pienso que es bueno que haga ese tipo de ejercicio, porque mi cuerpo lo necesita. 
Creo que tengo un polo manga zero, y veo mis músculos incipientes. 

Luego llega la noche y tengo que dormir. Es un hotel, tipo ibis. 
Resulta que hay una mujer durmiendo en mi cuarto. 
Una desconocida. La luz está oscura y de pronto me dice "quieres hacer el amor ?" en francés. 
Yo dudo porque es hermosa, blanca de piel pálida pero con facciones argelinas. 
Pero tiene el cuerpo como cansado y fláccido, como después de muchos sufrimientos y partos. 
Tengo temor de contagiarme, pero luego me doy cuenta de que tengo un preservativo y acepto. 
Ella me dice "que quieres que te haga ?". Entonces, adivinando mis deseos, se da vuelta y me ofrece su cuerpo. 
Yo me doy cuenta que tiene una cicatriz reciente, todavía roja, sobre el muslo izquierdo. 
Parece una operación. Entonces, dudo y no consigo penetrarla. 

Luego, al día siguiente la mujer se ha ido. Ha sido un mal sueño me digo. Menos mal que no eyaculé. 
De pronto me doy cuenta que tengo el pantalón de pijama verde completamente mojado. Comprendo que he tenido 
un sueño húmedo. Pero siento alivio de que fue tan solo un sueño. 

En ese momento me encuentro con un amigo, con el cabello rubio teñido, que me dice que sabe que estoy buscando 
formas de ganar dinero. 
Me dice que trabaja en un prostíbulo masculino y que le pagan 30 euros por sesión. 
Me muestra su tarjeta, que tiene caracteres dorados, como chinos. Me parece bastante dinero. 
Entonces, yo le pregunto "Cómo haces si no consigues tener una erección ?" 
El me dice que tiene derecho a rechazar de vez en cuando. 

Toda esa conversación hace que me sienta muy sucio. Busco un lugar para ducharme. 
Encuentro un baño público, en una estación de trenes en París. Está muy sucio, 
como cubierto por una pátina de sarro y calcáreo. Por fin encuentro la ducha, al fondo a la derecha. 
Quiero cerrar la puerta pero no lo consigo. No hay pestillo. Todo está muy sucio y no consigo bañarme...
Tengo la sensación de que, en cualquier momento, puede entrar una persona. 
Siento ansiedad. 

